    private void BtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegistrarActionPerformed
        String rol = cmbRol.getSelectedItem().toString();

        try {

            if (txtNombreUsu.getText().isEmpty()) {

                JOptionPane.showMessageDialog(null, "Campo nombre no puede estar vacio");

            } else if (TxtContra.getText().isEmpty()) {

                JOptionPane.showMessageDialog(null, "Campo contrase単a no puede estar vacio");

            } else if (rol.equals("Seleccione rol")) {
                JOptionPane.showMessageDialog(null, "Debes seleccionar un rol");

            } else if (!TxtContra.getText().equals(TxtRepeContra.getText())) {
                
                JOptionPane.showMessageDialog(null, "Las contraase単as no coinciden");

            } else {

                PreparedStatement ps = cn.prepareStatement(" INSERT INTO usuarios(usuario,contrasenia,tipo_nivel) VALUES (?,?,?)");

                ps.setString(1, txtNombreUsu.getText());
                ps.setString(2, TxtContra.getText());
                ps.setString(3, rol);
                ps.executeUpdate();

                JOptionPane.showMessageDialog(null, "Datos guardados");

            }

        } catch (SQLException e) {
            System.err.println(e);
            JOptionPane.showMessageDialog(null, "Error al guardar datos de notas");

        }


    }//GEN-LAST:event_BtnRegistrarActionPerformed

