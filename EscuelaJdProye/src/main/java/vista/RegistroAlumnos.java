    private void TxtNombreAluKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtNombreAluKeyTyped

        char c = evt.getKeyChar();
        if ((c < 'a' || c > 'z') && (c < 'A') | c > 'Z' && c != KeyEvent.VK_SPACE) {
            evt.consume();
        }

    }//GEN-LAST:event_TxtNombreAluKeyTyped

