    void mostrarTabla(String valor) {

        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("id");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("Matricula");
        modelo.addColumn("Materia");

        tablaRegistroAlu.setModel(modelo);

        String sql = "SELECT * FROM alumnos WHERE CONCAT (nombre,' ',apellido, ' ', matricula, ' ',id_curso_asignado) LIKE'%" + valor + "%'";

        String datos[] = new String[5];

        Statement st;

        try {

            st = cn.createStatement();

            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);

                modelo.addRow(datos);
            }

            tablaRegistroAlu.setModel(modelo);

        } catch (SQLException e) {
            System.err.println(e);
        }

    }

    public void cargarComboCurso(JComboBox comboDelCurso) {

        try {

            String sql = "SELECT NombreCurso FROM cursos";

            Statement st = cn.createStatement();

            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {

                comboDelCurso.addItem(rs.getString("NombreCurso"));
            }

        } catch (SQLException e) {
            System.err.println(e);
        }

    }

