    private void BtnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnActualizarActionPerformed
        String materia = (String) cmbMateria.getSelectedItem();

        try {
            if (TxtNombreAlu.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo nombre no puede quedar vacio");
            } else if (txtApellido.getText().isEmpty()) {

                JOptionPane.showMessageDialog(null, " Campo apellido no puede quedar vacio");
            } else if (TxtMatricula.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo matricula no puede quedar vacio");
            } else if (materia.equals("Seleccione materia")) {
                JOptionPane.showMessageDialog(null, "Debes seleccionar una materia");
            } else {
                PreparedStatement ps = cn.prepareStatement("UPDATE alumnos SET nombre='" + TxtNombreAlu.getText() + "', apellido='" + txtApellido.getText() + "',matricula='" + TxtMatricula.getText() + "',id_curso_asignado='" + cmbMateria.getSelectedItem().toString() + "' WHERE id_alumno='" + TxtIDAlumno.getText() + "'");

                int respuesta = ps.executeUpdate();

                if (respuesta > 0) {
                    JOptionPane.showMessageDialog(null, "Datos del alumno actualizados correctamente ");
                    limpiar();
                    mostrarTabla("");
                } else {
                    JOptionPane.showMessageDialog(null, "No se ha seleccionado fila");
                }

            }

        } catch (SQLException e) {
            System.err.println(e);
            JOptionPane.showMessageDialog(null, "Error al actualizar los datos... contacte al administrador");

        }


    }//GEN-LAST:event_BtnActualizarActionPerformed

