    private void BtnAgregrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregrarActionPerformed

        String id_curso_asignado = cmbMateria.getSelectedItem().toString();
        String materia = (String) cmbMateria.getSelectedItem();

        try {

            if (TxtNombreAlu.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo nombre no puede quedar vacio");

            } else if (txtApellido.getText().isEmpty()) {

                JOptionPane.showMessageDialog(null, " Campo apellido no puede quedar vacio");
            } else if (TxtMatricula.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo matricula no puede quedar vacio");
            } else if (materia.equals("Seleccione materia")) {
                JOptionPane.showMessageDialog(null, "Debes seleccionar una materia");
            } else {
                PreparedStatement ps = cn.prepareStatement("INSERT INTO alumnos (nombre,apellido,matricula,id_curso_asignado) VALUES (?,?,?,?)");
                ps.setString(1, TxtNombreAlu.getText());
                ps.setString(2, txtApellido.getText());
                ps.setString(3, TxtMatricula.getText());
                ps.setString(4, id_curso_asignado);
                ps.executeUpdate();

                JOptionPane.showMessageDialog(null, "Alumno registrado correctamente");
                mostrarTabla("");
                limpiar();

                for (int i = 0; i <= tablaRegistroAlu.getRowCount(); i++) {

                    TxtCantidad.setText(" " + i);

                }
            }

        } catch (SQLException e) {
            System.err.println(e);

            JOptionPane.showMessageDialog(null, "Error al guardar alumno");

        }


    }//GEN-LAST:event_BtnAgregrarActionPerformed

